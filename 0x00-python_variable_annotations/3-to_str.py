#!/usr/bin/env python3
"""
Module 3-to_str
Defines a function to_str which takes a float and returns its string representation.
"""


def to_str(n: float) -> str:
    """
    Return the string representation of the float n.

    Args:
        n (float): The float number to convert to a string.

    Returns:
        str: The string representation of the float number.
    """
    return str(n)
